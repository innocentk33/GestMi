package beans;

public class Matiere {
    private String nom_matiere;
    private String coif_matiere;

    public String getNom_matiere() {
        return nom_matiere;
    }

    public void setNom_matiere(String nom_matiere) {
        this.nom_matiere = nom_matiere;
    }

    public String getCoif_matiere() {
        return coif_matiere;
    }

    public void setCoif_matiere(String coif_matiere) {
        this.coif_matiere = coif_matiere;
    }
}
