package beans;

public class Etudiant {
    private String nom_etudiant;
    private String prenom_etudiant;
    private String matricul_etudiant;
    private String dat_etudiant;
    private String lieu_etudiant;
    private String adresse_etudiant;
    private String tel_etudiant;


    public String getNom_etudiant() {
        return nom_etudiant;
    }

    public void setNom_etudiant(String nom_etudiant) {
        this.nom_etudiant = nom_etudiant;
    }

    public String getPrenom_etudiant() {
        return prenom_etudiant;
    }

    public void setPrenom_etudiant(String prenom_etudiant) {
        this.prenom_etudiant = prenom_etudiant;
    }

    public String getMatricul_etudiant() {
        return matricul_etudiant;
    }

    public void setMatricul_etudiant(String matricul_etudiant) {
        this.matricul_etudiant = matricul_etudiant;
    }

    public String getDat_etudiant() {
        return dat_etudiant;
    }

    public void setDat_etudiant(String dat_etudiant) {
        this.dat_etudiant = dat_etudiant;
    }

    public String getLieu_etudiant() {
        return lieu_etudiant;
    }

    public void setLieu_etudiant(String lieu_etudiant) {
        this.lieu_etudiant = lieu_etudiant;
    }

    public String getAdresse_etudiant() {
        return adresse_etudiant;
    }

    public void setAdresse_etudiant(String adresse_etudiant) {
        this.adresse_etudiant = adresse_etudiant;
    }

    public String getTel_etudiant() {
        return tel_etudiant;
    }

    public void setTel_etudiant(String tel_etudiant) {
        this.tel_etudiant = tel_etudiant;
    }


}
